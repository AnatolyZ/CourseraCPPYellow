
#include <iostream>
#include <map>
#include <vector>
#include <tuple>
#include <string>
#include <algorithm>

using namespace std;

enum class Lang {
  DE, FR, IT
};

struct Region {
  string std_name;
  string parent_std_name;
  map<Lang, string> names;
  int64_t population;
};

tuple <const string&, const string& ,const map<Lang, string>&, const int64_t&>
get_fusion(const Region& r){
	return tie(r.std_name, r.parent_std_name, r.names, r.population);
}

bool operator== (const Region& lhs, const Region& rhs){
	return get_fusion(lhs) == get_fusion(rhs);
}

int FindMaxRepetitionCount(const vector<Region>& regions){
	if (regions.size() == 0) return 0;
	int result = 1;
	for(const auto& i:regions){
		int c = count(regions.begin(),regions.end(),i);
		if (result < c){
			result = c;
		}
	}
	return result;
};


